void USART2_IRQHandler2(void)
{
	uint8_t rxCaracter;
	BaseType_t xHigherPriorityTaskWoken = pdFALSE;

	if(__HAL_UART_GET_FLAG(&huart2,UART_FLAG_RXNE))
	{
		rxCaracter = USART_ReceiveData(USART2);
		rxData[rxCount++] = rxCaracter & 0xFF;
		if(rxCaracter == 10)
		{
			command_lenght = 0;
			xTaskNotifyFromISR(myTask02Handle,0, eNotifyAction, &xHigherPriorityTaskWoken);
			xTaskNotifyFromISR(defaultTaskHandle,0, eNotifyAction, &xHigherPriorityTaskWoken);
			rxCount = 0;
		}

		  __HAL_UART_CLEAR_FLAG(&huart2, UART_FLAG_RXNE);
	}

	if(xHigherPriorityTaskWoken == pdTRUE)
	{
		taskYIELD();
	}
}

